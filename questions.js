function makeGeneratedQuestion(id, lec, star, q, choices, explain) {
  return { id, lec, star, q, choices, answer: 0, explain };
}

function buildGeneratedLectureQuestions(lecture) {
  const topicQuestion = makeGeneratedQuestion(
    `${lecture.id}-01`,
    lecture.lec,
    2,
    `${lecture.lec} 강의의 핵심 내용으로 가장 알맞은 것은?`,
    [
      lecture.topic,
      "강의 영상 다운로드와 재생 오류 해결 절차",
      "시험 일정과 행정 공지만 정리하는 내용",
      "배우 개인 신상과 공연 홍보 자료만 나열하는 내용"
    ],
    `${lecture.lec} 강의는 ${lecture.topic}을 중심으로 정리한다.`
  );

  const termQuestions = lecture.terms.map((term, idx) => {
    const wrongs = lecture.terms.map(item => item[0]).filter(name => name !== term[0]).slice(0, 3);
    return makeGeneratedQuestion(
      `${lecture.id}-${String(idx + 2).padStart(2, "0")}`,
      lecture.lec,
      idx % 2 === 0 ? 2 : 3,
      `"${term[1]}"에 해당하는 개념은?`,
      [term[0], ...wrongs],
      `${term[0]}은 ${term[1]}`
    );
  });

  return [topicQuestion, ...termQuestions];
}

window.SOURCE_ORDER.push(...GENERATED_LECTURES.map(lecture => lecture.lec));
window.QUESTION_POOL.push(...GENERATED_LECTURES.flatMap(buildGeneratedLectureQuestions));
